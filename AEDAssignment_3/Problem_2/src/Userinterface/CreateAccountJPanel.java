    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if("".equals(txtName.getText().trim())||"".equals(txtSSN.getText().trim())||
            "".equals(txtPhoneNum.getText().trim())||"".equals(txtAddress.getText().trim())){
            JOptionPane.showMessageDialog(null, "Please complete all information !","Error",
                JOptionPane.WARNING_MESSAGE);
        }
        else if(!Letter(txtName.getText().trim())){
            JOptionPane.showMessageDialog(null, "Invalid name!");
        }
        else if(!isNumeric(txtSSN.getText().trim()) ){
            JOptionPane.showMessageDialog(null, "Invalid SSN!");

        }
        else if(!isNumeric(txtPhoneNum.getText().trim()) ){
            JOptionPane.showMessageDialog(null, "Invalid phone number!");
        }
        else{
            Account account = accountDirectory.addAccount();
            account.setName(txtName.getText().trim());
            account.setSSN(txtSSN.getText().trim());
            account.setPhoneNumber(txtPhoneNum.getText().trim());
            account.setAddress(txtAddress.getText().trim());
           

            JOptionPane.showMessageDialog(null, "Patient Information saved successfully");

            txtName.setText("");
            txtSSN.setText("");
            txtPhoneNum.setText("");
            txtAddress.setText("");
           

        }
    }//GEN-LAST:event_btnSaveActionPerformed

