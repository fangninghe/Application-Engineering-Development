    private void btnSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave1ActionPerformed
        // TODO add your handling code here:

        if("".equals(txtID.getText().trim())|| "".equals(txtAge.getText().trim())||"".equals(txtDocName.getText().trim())||
            "".equals(txtPhaName.getText().trim())){
            JOptionPane.showMessageDialog(null, "Please complete all information !","Error",
                JOptionPane.WARNING_MESSAGE);
        }

      
        else if(!isNumeric(txtAge.getText().trim()) ){
            JOptionPane.showMessageDialog(null, "Invalid age!");

        }

        else if(!Letter(txtDocName.getText().trim())){
            JOptionPane.showMessageDialog(null, "Invalid primary doctor name!");
        }

        else if(!Letter(txtPhaName.getText().trim())){
            JOptionPane.showMessageDialog(null, "Invalid preferred pharmacy name!");
        }

        else{
            
            PatientInfo pif = account.setItAsPatient();

            pif.setID(txtID.getText().trim());
            pif.setAge(Integer.parseInt(txtAge.getText().trim()));
            pif.setDocName(txtDocName.getText().trim());
            pif.setPhaName(txtPhaName.getText().trim());

            JOptionPane.showMessageDialog(null, "Patient Information saved successfully");

          
            txtDocName.setText("");
            txtID.setText("");
            txtAge.setText("");
            txtPhaName.setText("");
            

        }
    }//GEN-LAST:event_btnSave1ActionPerformed

