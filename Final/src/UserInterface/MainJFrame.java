    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String userName = txtUserName.getText();
        char[] passwordCharArray = txtPassword.getPassword();
        String password = String.valueOf(passwordCharArray);
        boolean flag = false;

        //UserAccount userAccount = null;
        for (Organization organization : business.getOrganizationDirectory().getOrganizationList()) {
            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
            if (userAccount != null) {

                try {
                    Session session = userAccount.getSessionHistory().addSession();
                    business.getSessionBackup().add(session);
                    session.setUserAccount(userAccount);
                    Activity activity = session.getLogIn();
                    activity.setUserAccount(userAccount);
                    InetAddress ip = InetAddress.getLocalHost();
                    String IP = ip.getHostAddress();
                    session.setIP(IP);
                   
                } catch (UnknownHostException ex) {
                    Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }

                int lastSession = userAccount.getSessionHistory().getSessionList().size() - 2;
                if (lastSession >= 0) {
                    Session lastsession = userAccount.getSessionHistory().getSessionList().get(lastSession);
                    String lastLogin = lastsession.getLogIn().getTime();

                    txtLastLogin.setText(lastLogin);
                } else {
                    txtLastLogin.setText(" ");
                }
                
//                Session lastsession = userAccount.getSessionHistory().getSessionList().get(lastSession);
//                String lastLogin = lastsession.getLogIn().getTime();
//
//                txtLastLogin.setText(lastLogin);

               // if(userAccount.isIsActive()){
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                userProcessContainer.add("workArea", userAccount.getRole().createWorkArea(userProcessContainer, userAccount, organization, business, dB4OUtil));
                layout.next(userProcessContainer);
                flag = true;
                break;
//                }else{
//                    JOptionPane.showMessageDialog(null, "Your useraccount is inactive!");
//                    return;
//                }
                
            }
        }
       
        if (flag == false) {
            JOptionPane.showMessageDialog(null, "Invalid User Name/ Password.");
            return;
        }

        btnLogin.setEnabled(false);
        btnLogout.setEnabled(true);
        txtUserName.setEnabled(false);
        txtPassword.setEnabled(false);
        
         dB4OUtil.storeSystem(business);


    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutActionPerformed
        btnLogout.setEnabled(false);
        txtUserName.setEnabled(true);
        txtPassword.setEnabled(true);
        btnLogin.setEnabled(true);

        txtUserName.setText("");
        txtPassword.setText("");
        txtLastLogin.setText("");

        int lastSession = userAccount.getSessionHistory().getSessionList().size() - 1;
        Session session = userAccount.getSessionHistory().getSessionList().get(lastSession);
        Activity activity = session.getActivityHistory().addActivity();//new Activity();
        activity.setName("Log out");
        activity.setStatus("Normal");
        

        session.setLogOut(activity);

        userProcessContainer.removeAll();
        JPanel blankJP = new JPanel();
        userProcessContainer.add("blank", blankJP);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);

        dB4OUtil.storeSystem(business);
    }//GEN-LAST:event_btnLogoutActionPerformed

