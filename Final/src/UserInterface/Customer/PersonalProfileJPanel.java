    private void btnBuyVipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyVipActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to buy a VIP?", "Information", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION){
            customer.setStatus("VIP");
            txtSatus.setText(customer.getStatus());
        }
    }//GEN-LAST:event_btnBuyVipActionPerformed


