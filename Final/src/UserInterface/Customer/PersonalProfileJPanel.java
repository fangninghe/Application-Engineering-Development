    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String address = txtAddress.getText();
            String email = txtEmail.getText();
            String phone = txtPhone.getText();

            customer.setAddress(address);
            customer.setEmail(email);
            customer.setPhone(phone);

            JOptionPane.showMessageDialog(null, "Profile successfully updated.", "Information", JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Correct format required.", "Information", JOptionPane.ERROR_MESSAGE);
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        userProcessContainer.remove(this);
        CustomerWorkAreaJPanel panel = (CustomerWorkAreaJPanel) userProcessContainer.getComponent(userProcessContainer.getComponentCount() - 1);
        panel.populateProfile();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer); // TODO add your handling code here:
    }//GEN-LAST:event_btnBackActionPerformed

