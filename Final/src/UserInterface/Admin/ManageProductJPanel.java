    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = ProductTable.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the product?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Product p = (Product) ProductTable.getValueAt(selectedRow, 1);
                if (p.getAvailablity() > 0) {
                    JOptionPane.showMessageDialog(null, "Still in Stock!! Cannot delete!!", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    inventory.deleteProduct(p);
                    populateProductTable();
                    JOptionPane.showMessageDialog(null, "Product deleted successfully");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

