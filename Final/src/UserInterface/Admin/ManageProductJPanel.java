    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (txtSearch.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Search is empty!");
        } else {
            int id = Integer.parseInt(txtSearch.getText().trim());
            Product p = inventory.searchProduct(id);

            if (p == null) {
                JOptionPane.showMessageDialog(null, "Product not found");
                return;
            } else {
                ViewProductJPanel viewProduct = new ViewProductJPanel(userProcessContainer, p);
                userProcessContainer.add("ViewProduct", viewProduct);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnSearchActionPerformed

