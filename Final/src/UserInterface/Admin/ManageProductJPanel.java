    public void populateProductTable() {
        int i = 1;
        DefaultTableModel dtm = (DefaultTableModel) ProductTable.getModel();
        dtm.setRowCount(0);
        for (Product p : inventory.getProductList()) {
            Object row[] = new Object[8];
            row[0] = i;
            row[1] = p;
            row[2] = p.getModelNumber();
            row[3] = p.getAvailablity();
            row[4] = p.getProfit();
            row[5] = p.getFloorPrice();
            row[6] = p.getTargetPrice();
            row[7] = p.getCeilingPrice();

            dtm.addRow(row);
            i++;
        }
    }
    
    public void sortProduct() {
        Collections.sort(inventory.getProductList(), new sort());
    }

    class sort implements Comparator {

        public int compare(Object o1, Object o2) {
           
            Product p1 = (Product) o1;
            Product p2 = (Product) o2;
            if (p1.getProfit() > p2.getProfit()) {
                return -1;
            } else if (p1.getProfit() < p2.getProfit()) {
                return 1;
            }
            return 0;
        }
    }

