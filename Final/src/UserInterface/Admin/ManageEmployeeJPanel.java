    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        if (txtName.getText().trim().isEmpty() || txtSSN.getText().trim().isEmpty()
                || txtPhone.getText().trim().isEmpty() || txtAddress.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name, Phone Number, Address, and ssn cannot be Empty!!");
        } else if (!isNumeric(txtPhone.getText().trim())) {
            JOptionPane.showMessageDialog(null, "Invalid Phone Number!");
        } else if (!isNumeric(txtSSN.getText().trim())) {
            JOptionPane.showMessageDialog(null, "Invalid SSN Number!");
        } else {
            Organization organization = (Organization) ChooseOrgComboBox.getSelectedItem();

            if (organization.getName().equals("Sale Organization")) {
                Employee employee = organization.getEmployeeDirectory().createEmployee(Employee.Type.Sale, txtName.getText().trim(), txtSSN.getText().trim(),
                        txtPhone.getText().trim(), txtAddress.getText().trim(), txtEmail.getText().trim());
            }else if (organization.getName().equals("Security Organization")) {
                Employee employee = organization.getEmployeeDirectory().createEmployee(Employee.Type.Security, txtName.getText().trim(), txtSSN.getText().trim(),
                        txtPhone.getText().trim(), txtAddress.getText().trim(), txtEmail.getText().trim());
            }

            JOptionPane.showMessageDialog(null, "Employee Added Successfully", "Information", JOptionPane.INFORMATION_MESSAGE);

            //populateEmployeeTable(organization);

            txtName.setText("");
            txtSSN.setText("");
            txtAddress.setText("");
            txtEmail.setText("");
            txtPhone.setText("");

        }
    }//GEN-LAST:event_btnCreateActionPerformed

