    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = EmployeeTable.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the employee?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Employee e = (Employee) EmployeeTable.getValueAt(selectedRow, 0);
                Organization organization = (Organization) ChooseOrgComboBox.getSelectedItem();
                organization.getEmployeeDirectory().deleteEmployee(e);
                populateEmployeeTable(organization);
                JOptionPane.showMessageDialog(null, "Employee deleted successfully");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaleActionPerformed
        Organization organization = (Organization) OrganizationCombox.getSelectedItem();

        if (organization.getName().equals("Sale Organization")) {
            int selectedRow = EmployeeTable.getSelectedRow();

            if (selectedRow < 0) {
                JOptionPane.showMessageDialog(null, "Please select a row from the table first to view detail", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                Sale s = (Sale) EmployeeTable.getValueAt(selectedRow, 0);
                ViewSaleJPanel panel = new ViewSaleJPanel(userProcessContainer, s);
                userProcessContainer.add("ViewSale", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from the Sale Organization", "Warning", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnSaleActionPerformed

