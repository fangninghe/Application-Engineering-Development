    public boolean isNumeric(String str) {

        for (int i = str.length(); --i >= 0;) {
            if (!Character.isDigit(str.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    private void populateOrganizationCombox() {
        OrganizationCombox.removeAllItems();
        for (Organization organization : organizationDirectory.getOrganizationList()) {
            if (!organization.getName().equals("Customer Organization")) {
                OrganizationCombox.addItem(organization);
            }
        }
    }

    private void populateChooseOrgCombox() {
        ChooseOrgComboBox.removeAllItems();
        for (Organization organization : organizationDirectory.getOrganizationList()) {
            if (!organization.getName().equals("Customer Organization")) {
                ChooseOrgComboBox.addItem(organization);
            }
        }
    }

    private void populateEmployeeTable(Organization organization) {
        DefaultTableModel model = (DefaultTableModel) EmployeeTable.getModel();

        model.setRowCount(0);

        for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()) {
            Object[] row = new Object[6];
            row[0] = employee;
            row[1] = employee.getId();
            row[2] = employee.getSsn();
            row[3] = employee.getPhone();
            row[4] = employee.getAddress();
            row[5] = employee.getEmail();

            model.addRow(row);
        }
    }
    
    public void sortSale(Organization organization) {
        Collections.sort(organization.getEmployeeDirectory().getEmployeeList(), new sort());
    }

    class sort implements Comparator {

        public int compare(Object o1, Object o2) {

            Sale S1 = (Sale) o1;
            Sale S2 = (Sale) o2;
            if (S1.getProfit() > S2.getProfit()) {
                return -1;
            } else if (S1.getProfit() < S2.getProfit()) {
                return 1;
            }
            return 0;
        }
    }

