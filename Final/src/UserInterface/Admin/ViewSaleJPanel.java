    public void populatOrderTable() {
        int i = 1;
        DefaultTableModel dtm = (DefaultTableModel) OrderTable.getModel();
        dtm.setRowCount(0);
        for (Order o : sale.getSaleOrderCatalog().getSaleOrderCatalog()) {
            Object row[] = new Object[4];
            row[0] = i;
            row[1] = o;
            row[2] = o.getCustomer().getName();
            row[3] = o.getStatus();

            dtm.addRow(row);
            i++;
        }
    }
    
    public void populateCommission() {
        if (sale.getProfit() <= 3000) {
            sale.setCommission("Level1: 2% of Profit");
            txtCommission.setText(String.valueOf(sale.getProfit() * 0.02));
        } else if (sale.getProfit() > 3000 && sale.getProfit() <= 10000) {
            sale.setCommission("Level2: 4% of Profit");
            txtCommission.setText(String.valueOf(sale.getProfit() * 0.04));
        } else if (sale.getProfit() > 10000 && sale.getProfit() <= 20000) {
            sale.setCommission("Level3: 6% of Profit");
            txtCommission.setText(String.valueOf(sale.getProfit() * 0.06));
        } else if (sale.getProfit() > 20000) {
            sale.setCommission("Level4: 8% of Profit");
            txtCommission.setText(String.valueOf(sale.getProfit() * 0.08));
        }
    }
    
    public void totalAmount() {
        double sum = 0;

        for (Order o : sale.getSaleOrderCatalog().getSaleOrderCatalog()) {
            sum += o.getAmount();

        }
        txtTotalAmount.setText(String.valueOf(sum));
    }

