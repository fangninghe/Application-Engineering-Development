    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int selectedRow = OrderTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first to view detail", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            Order order = (Order) OrderTable.getValueAt(selectedRow, 1);
            DefaultTableModel dtm = (DefaultTableModel) OrderItemTable.getModel();
            dtm.setRowCount(0);
            for (OrderItem oi : order.getOrderItemList()) {
                Object row[] = new Object[4];
                row[0] = oi;
                row[1] = oi.getQuantity();
                row[2] = oi.getSalesPrice();
                row[3] = oi.getProduct().getTargetPrice();

                dtm.addRow(row);

            }

        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String name = txtName.getText();
            String address = txtAddress.getText();
            String email = txtEmail.getText();
            String phone = txtPhoneNumber.getText();

            sale.setName(name);
            sale.setAddress(address);
            sale.setEmail(email);
            sale.setPhone(phone);

            JOptionPane.showMessageDialog(null, "Sale successfully updated.", "Information", JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Correct format required.", "Information", JOptionPane.ERROR_MESSAGE);
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

