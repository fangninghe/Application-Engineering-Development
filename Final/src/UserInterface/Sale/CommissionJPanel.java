    public void populateCommission(){
       if(sale.getProfit()<=3000 ){
           sale.setCommission("Level1: 2% of Profit: " + sale.getProfit()*0.02);
       }
       else if(sale.getProfit()>3000 && sale.getProfit()<=10000){
           sale.setCommission("Level2: 4% of Profit: " + sale.getProfit()*0.04);
       }else if(sale.getProfit()>10000 && sale.getProfit()<=20000){
           sale.setCommission("Level3: 6% of Profit: " + sale.getProfit()*0.06);
       }else if(sale.getProfit()>20000){
           sale.setCommission("Level4: 8% of Profit: " + sale.getProfit()*0.08);
       }
       
       txtCommission.setText(sale.getCommission());
   }

